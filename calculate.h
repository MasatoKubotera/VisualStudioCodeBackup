/*桁管理*/
void NumSetup(int x,double *Num,int Time,int Flag,int DigitNum){
	if(Flag == 0)
		Num[Time] = Num[Time] * 10 + x;
	else
        Num[Time] += x * pow(10,-(DigitNum+1));
    return;
}
