/*数式入力*/
void FormulaInput(char *Str){
    /*数式を入力してください*/
    printf("数式かコマンドを入力してください : ");

    /*英数字・記号 読み込み*/
    unsigned int i = 0;
    do{
    	Str[i] = getchar();
    } while(Str[i++] != 0x0A); //0x0A:Ent

    /*文字列の最後を必ず'='にする*/
    Str[i-1] = (Str[i-2] == '=' ? 0 :'=');
}
