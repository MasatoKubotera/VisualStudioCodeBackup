/*空白をなくし式を短くする*/
void Shorten(int *NumFlag, char *Sign, double *Num){
    for(int i = 0; ; i++){
        if(Sign[i] == '=')
            break;
        else
        if(NumFlag[i] == 0 && Sign[i] == 0){
            for(int j = i; ; j++){
                if(NumFlag[j] == 0 && Sign[j] == 0){
                    Num[j] = Num[j+1];
                    Num[j+1] = 0; 

                    NumFlag[j] = (NumFlag[j+1] == 1 ? 1 :0);
                    NumFlag[j+1] = 0;

                    Sign[j] = Sign[j+1];
                    Sign[j+1] = 0;
                    
                    if(Sign[j] == '=')
                        break;
                }
            }
        }
        if(Sign[i] == '=')
            break;
    }
    return;
}
